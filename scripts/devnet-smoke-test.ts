/**
 * SSS Devnet Smoke Test
 *
 * Exercises all major SSS operations against Solana devnet and records
 * transaction signatures. Results are written to DEVNET_EVIDENCE.md.
 *
 * Usage:
 *   ANCHOR_WALLET=~/.config/solana/id.json \
 *   ANCHOR_PROVIDER_URL=https://api.devnet.solana.com \
 *   npx ts-node scripts/devnet-smoke-test.ts
 */

import * as anchor from "@coral-xyz/anchor";
import {
  Connection,
  Keypair,
  PublicKey,
  LAMPORTS_PER_SOL,
} from "@solana/web3.js";
import {
  getOrCreateAssociatedTokenAccount,
  TOKEN_2022_PROGRAM_ID,
  transferCheckedWithTransferHook,
} from "@solana/spl-token";
import BN from "bn.js";
import * as fs from "fs";
import * as path from "path";
import * as os from "os";

import { SolanaStablecoin } from "../sdk/src/core/stablecoin";
