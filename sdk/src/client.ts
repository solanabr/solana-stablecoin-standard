  // ═══════════════════════════════════════════════════════════════════════
  // Queries
  // ═══════════════════════════════════════════════════════════════════════

  /**
   * Fetch the on-chain stablecoin configuration.
   */
  async getConfig(): Promise<StablecoinConfig> {
    const accountInfo = await this.connection.getAccountInfo(this.configPda);
    if (!accountInfo) {
      throw new Error(`Config account not found: ${this.configPda.toBase58()}`);
    }
    return this.deserializeConfig(accountInfo.data);
  }

  /**
   * Fetch the on-chain role assignments.
   */
  async getRoles(): Promise<RoleManager> {
    const accountInfo = await this.connection.getAccountInfo(this.rolesPda);
    if (!accountInfo) {
      throw new Error(`Roles account not found: ${this.rolesPda.toBase58()}`);
    }
    return this.deserializeRoles(accountInfo.data);
  }

  /**
   * Get the net token supply (totalMinted - totalBurned).
   */
  async getTotalSupply(): Promise<bigint> {
    const config = await this.getConfig();
    return config.totalMinted - config.totalBurned;
  }

  /**
   * Get the mint public key.
   */
  getMint(): PublicKey {
    return this.mintAddress;
  }

  /**
   * Get the config PDA public key.
   */
  getConfigPda(): PublicKey {
    return this.configPda;
  }

  /**
   * Get the roles PDA public key.
   */
  getRolesPda(): PublicKey {
    return this.rolesPda;
  }

  // ═══════════════════════════════════════════════════════════════════════
  // Compliance Module (SSS-2)
  // ═══════════════════════════════════════════════════════════════════════

  /**
   * Compliance module for SSS-2 operations.
   *
   * These operations only work if the stablecoin was initialized with
   * `enablePermanentDelegate: true` and `enableTransferHook: true`.
   */
  readonly compliance: ComplianceModule = {
    /**
     * Add an address to the blacklist.
     * Creates a blacklist PDA on-chain. The signer must be the blacklister
     * or master authority.
     */
    blacklistAdd: async (params: BlacklistAddParams): Promise<string> => {
      const [blacklistPda] = deriveBlacklistPda(
        this.configPda,
        params.address,
        this.programId
      );

      const ix = this.buildInstruction("add_to_blacklist", {
        reason: params.reason,
      }, {
        blacklister: this.wallet.publicKey,
        config: this.configPda,
        roleManager: this.rolesPda,
        blacklistEntry: blacklistPda,
        addressToBlacklist: params.address,
        systemProgram: SystemProgram.programId,
      });

      const tx = new Transaction().add(ix);
      return sendAndConfirmTransaction(
        this.connection,
        tx,
        [this.wallet.payer],
        { commitment: "confirmed" }
      );
    },

    /**
     * Remove an address from the blacklist.
     * Closes the blacklist PDA and reclaims rent.
     */
    blacklistRemove: async (address: PublicKey): Promise<string> => {
      const [blacklistPda] = deriveBlacklistPda(
        this.configPda,
        address,
        this.programId
      );

      const ix = this.buildInstruction("remove_from_blacklist", {}, {
        blacklister: this.wallet.publicKey,
        config: this.configPda,
        roleManager: this.rolesPda,
        blacklistEntry: blacklistPda,
      });

      const tx = new Transaction().add(ix);
      return sendAndConfirmTransaction(
        this.connection,
        tx,
        [this.wallet.payer],
        { commitment: "confirmed" }
      );
    },

    /**
     * Check if an address is blacklisted.
     * Checks if the blacklist PDA exists on-chain.
     */
    isBlacklisted: async (address: PublicKey): Promise<boolean> => {
      const [blacklistPda] = deriveBlacklistPda(
        this.configPda,
        address,
        this.programId
      );
      const accountInfo = await this.connection.getAccountInfo(blacklistPda);
      return accountInfo !== null;
    },

    /**
     * Seize tokens from a frozen, blacklisted account.
     * Uses the permanent delegate authority to transfer tokens to treasury.
     */
    seize: async (from: PublicKey, treasury: PublicKey): Promise<string> => {
      const [blacklistPda] = deriveBlacklistPda(
        this.configPda,
        from,
        this.programId
      );

      const fromAta = getAssociatedTokenAddressSync(
        this.mintAddress,
        from,
        false,
        TOKEN_2022_PROGRAM_ID
      );

      const treasuryAta = getAssociatedTokenAddressSync(
        this.mintAddress,
        treasury,
        false,
        TOKEN_2022_PROGRAM_ID
      );

      const ix = this.buildInstruction("seize", {}, {
        seizer: this.wallet.publicKey,
        config: this.configPda,
        roleManager: this.rolesPda,
        blacklistEntry: blacklistPda,
        mint: this.mintAddress,
        fromTokenAccount: fromAta,
        treasuryTokenAccount: treasuryAta,
        tokenProgram: TOKEN_2022_PROGRAM_ID,
      });

      const tx = new Transaction().add(ix);
      return sendAndConfirmTransaction(
        this.connection,
        tx,
        [this.wallet.payer],
        { commitment: "confirmed" }
      );
    },
  };

