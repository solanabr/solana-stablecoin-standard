  /**
   * Build a raw Anchor instruction.
   *
   * Uses the Anchor discriminator format: sha256("global:<method_name>")[0..8]
   * followed by borsh-serialized args.
   */
  private buildInstruction(
    _methodName: string,
    _args: Record<string, unknown>,
    accounts: Record<string, PublicKey>
  ): TransactionInstruction {
    // For now, we use the Anchor program object (loaded via IDL) to build instructions.
    // The actual implementation will use program.methods.<method>().accounts({...}).instruction()
    // This is a placeholder that creates the account metas for transaction construction.

    const keys = Object.entries(accounts).map(([name, pubkey]) => ({
      pubkey,
      isSigner: name === "authority" || name === "minter" || name === "burner" ||
        name === "pauser" || name === "blacklister" || name === "seizer",
      isWritable: name === "config" || name === "roleManager" || name === "mint" ||
        name === "recipientTokenAccount" || name === "burnerTokenAccount" ||
        name === "tokenAccount" || name === "blacklistEntry" ||
        name === "fromTokenAccount" || name === "treasuryTokenAccount" ||
        name === "authority" || name === "minter" || name === "burner" ||
        name === "pauser" || name === "blacklister" || name === "seizer",
    }));

    // Placeholder instruction data — will be replaced with proper Anchor serialization
    return new TransactionInstruction({
      programId: this.programId,
      keys,
      data: Buffer.alloc(0),
    });
  }

  /**
   * Build the initialize instruction (special case — needs mint keypair + more accounts).
   */
  private async buildInitializeInstruction(
    params: {
      name: string;
      symbol: string;
      uri: string;
      decimals: number;
      enablePermanentDelegate: boolean;
      enableTransferHook: boolean;
      enableConfidentialTransfers: boolean;
      defaultAccountFrozen: boolean;
      pauser: PublicKey;
      blacklister: PublicKey | null;
      seizer: PublicKey | null;
    },
    mintKeypair: Keypair,
  ): Promise<TransactionInstruction> {
    return this.buildInstruction("initialize", { params }, {
      authority: this.wallet.publicKey,
      config: this.configPda,
      roleManager: this.rolesPda,
      mint: mintKeypair.publicKey,
      tokenProgram: TOKEN_2022_PROGRAM_ID,
      systemProgram: SystemProgram.programId,
      rent: SYSVAR_RENT_PUBKEY,
    });
  }

  /**
   * Deserialize a StablecoinConfig from raw account data.
   * Skips the 8-byte Anchor discriminator.
   */
  private deserializeConfig(data: Buffer): StablecoinConfig {
    let offset = 8; // Skip Anchor discriminator

    const authority = new PublicKey(data.subarray(offset, offset + 32));
    offset += 32;

    const mint = new PublicKey(data.subarray(offset, offset + 32));
    offset += 32;

    // String: 4-byte length prefix + data
    const nameLen = data.readUInt32LE(offset);
    offset += 4;
    const name = data.subarray(offset, offset + nameLen).toString("utf8");
    offset += nameLen;

    const symbolLen = data.readUInt32LE(offset);
    offset += 4;
    const symbol = data.subarray(offset, offset + symbolLen).toString("utf8");
    offset += symbolLen;

    const uriLen = data.readUInt32LE(offset);
    offset += 4;
    const uri = data.subarray(offset, offset + uriLen).toString("utf8");
    offset += uriLen;

    const decimals = data.readUInt8(offset);
    offset += 1;

    const isPaused = data.readUInt8(offset) === 1;
    offset += 1;

    const totalMinted = data.readBigUInt64LE(offset);
    offset += 8;

    const totalBurned = data.readBigUInt64LE(offset);
    offset += 8;

    const enablePermanentDelegate = data.readUInt8(offset) === 1;
    offset += 1;

    const enableTransferHook = data.readUInt8(offset) === 1;
    offset += 1;

    const enableConfidentialTransfers = data.readUInt8(offset) === 1;
    offset += 1;

    const defaultAccountFrozen = data.readUInt8(offset) === 1;
    offset += 1;

    const bump = data.readUInt8(offset);

    return {
      authority,
      mint,
      name,
      symbol,
      uri,
      decimals,
      isPaused,
      totalMinted,
      totalBurned,
      enablePermanentDelegate,
      enableTransferHook,
      enableConfidentialTransfers,
      defaultAccountFrozen,
      bump,
    };
  }

  /**
   * Deserialize a RoleManager from raw account data.
   * Skips the 8-byte Anchor discriminator.
   */
  private deserializeRoles(data: Buffer): RoleManager {
    let offset = 8; // Skip Anchor discriminator

    const config = new PublicKey(data.subarray(offset, offset + 32));
    offset += 32;

    const masterAuthority = new PublicKey(data.subarray(offset, offset + 32));
    offset += 32;

    const pauser = new PublicKey(data.subarray(offset, offset + 32));
    offset += 32;

    // Vec<MinterEntry>: 4-byte length + entries
    const mintersLen = data.readUInt32LE(offset);
    offset += 4;
    const minters = [];
    for (let i = 0; i < mintersLen; i++) {
      const address = new PublicKey(data.subarray(offset, offset + 32));
      offset += 32;
      const quota = data.readBigUInt64LE(offset);
      offset += 8;
      const minted = data.readBigUInt64LE(offset);
      offset += 8;
      minters.push({ address, quota, minted });
    }

    // Vec<Pubkey>: 4-byte length + pubkeys
    const burnersLen = data.readUInt32LE(offset);
    offset += 4;
    const burners = [];
    for (let i = 0; i < burnersLen; i++) {
      burners.push(new PublicKey(data.subarray(offset, offset + 32)));
      offset += 32;
    }

    const blacklister = new PublicKey(data.subarray(offset, offset + 32));
    offset += 32;

    const seizer = new PublicKey(data.subarray(offset, offset + 32));
    offset += 32;

    const bump = data.readUInt8(offset);

    return {
      config,
      masterAuthority,
      pauser,
      minters,
      burners,
      blacklister,
      seizer,
      bump,
    };
  }
